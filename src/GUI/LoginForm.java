    private void btnSigninActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSigninActionPerformed
        String password = new String(txtPassword.getPassword());
        try {
            
            p = perdoruesiRepository.findByUsername(txtUsername.getText(), password);
            if (p != null) {

                if (password.equals(p.getPassword())) {
                    if (p.getRoliID().getEmriRolit().equalsIgnoreCase("Admin")) {
                        MainForm am = new MainForm();
                        am.setVisible(true);
                        dispose();
                    } else if(p.getRoliID().getEmriRolit().equalsIgnoreCase("PuntorÃ«")){
                        MainForm am = new MainForm();
                        am.perdoruesiMenu.setVisible(false);
                        am.setVisible(true);
                        dispose();
                    } else if(p.getRoliID().getEmriRolit().equalsIgnoreCase("Menaxher")){
                        MainForm am = new MainForm();
                        am.automjetiMenu.setVisible(false);
                        am.shoferiMenu.setVisible(false);
                        am.setVisible(true);
                        dispose();
                    
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSigninActionPerformed

    private void txtPasswordKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyTyped
        char c = evt.getKeyChar();
        if(c == KeyEvent.VK_ENTER)
        {
            String password = new String(txtPassword.getPassword());
            try {           
 
                p = perdoruesiRepository.findByUsername(txtUsername.getText(), password);
                if (p != null) {

                    if (password.equals(p.getPassword())) {
                        if (p.getRoliID().getEmriRolit().equalsIgnoreCase("Admin")) {
                            MainForm am = new MainForm();
                            am.setVisible(true);
                            dispose();
                        } else {
                            MainForm am = new MainForm();
                            am.setVisible(true);
                            dispose();
                        }
                    }
                }
            }catch (Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_txtPasswordKeyTyped

