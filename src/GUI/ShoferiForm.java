    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            NumriTelefonitValidator v = new NumriTelefonitValidator();
            int row = jTable1.getSelectedRow();
            if (txtNrPatentes.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Ju lutem Shkruani Nr. e Patentes :", "Error", JOptionPane.ERROR_MESSAGE);
               
            } else if (txtEmri.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ju lutem Shkruani Emrin :", "Error", JOptionPane.ERROR_MESSAGE);
            }            
            else if (txtMbiemri.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ju lutem Shkruani Mbiemrin :", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else if (!v.validate(txtNrTelefonit.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Ju lutem Shkruani Numrin e Telefonit :", "Error", JOptionPane.ERROR_MESSAGE);
            }else {
                if (row == -1) {
                    Shoferi shoferi = new Shoferi();
                    shoferi.setNrPatentes(txtNrPatentes.getText());
                    shoferi.setEmri(txtEmri.getText());
                    shoferi.setMbiemri(txtMbiemri.getText());
                    shoferi.setNrTelefonit(txtNrTelefonit.getText());
                    shoferiRepository.create(shoferi);
                    JOptionPane.showMessageDialog(this, "E dhëna u ruajt me sukses !");
                } else {
                    Shoferi shoferi = this.shoferiTableModel.getShoferi(row);

                    shoferi.setNrPatentes(txtNrPatentes.getText());
                    shoferi.setEmri(txtEmri.getText());
                    shoferi.setMbiemri(txtMbiemri.getText());
                    shoferi.setNrTelefonit(txtNrTelefonit.getText());
                    shoferiRepository.edit(shoferi);
                    JOptionPane.showMessageDialog(this, "E dhëna u editua me sukses");
                }
                emptyObject();
                tabelaLoad();
            }

        } catch (ShoferiException pe) {
            JOptionPane.showMessageDialog(this, pe.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

