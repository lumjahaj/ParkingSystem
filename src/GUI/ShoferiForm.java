    private void tabelaLoad(){
        List<Shoferi> lista = shoferiRepository.findAll();
        shoferiTableModel.add(lista);
        jTable1.setModel(shoferiTableModel);
        shoferiTableModel.fireTableDataChanged();
        tabelaSelectedIndexChange();
    }
    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = jTable1.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Shoferi shoferi = shoferiTableModel.getShoferi(selectedIndex);

                    txtNrPatentes.setText(shoferi.getNrPatentes());
                    txtEmri.setText(shoferi.getEmri());
                    txtMbiemri.setText(shoferi.getMbiemri());
                    txtNrTelefonit.setText(shoferi.getNrTelefonit());
                    
                }
            }
        });
    }
