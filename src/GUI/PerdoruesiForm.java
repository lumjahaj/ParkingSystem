    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            int row = jTable1.getSelectedRow();
            if (txtUsername.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Ju lutem Shkruani Nr. e Patentes :", "Error", JOptionPane.ERROR_MESSAGE);

            } else if (txtPassword.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ju lutem Shkruani Emrin :", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                if (row == -1) {
                    Perdoruesi perdoruesi = new Perdoruesi();
                    perdoruesi.setUsername(txtUsername.getText());
                    perdoruesi.setPassword(txtPassword.getText());
                    perdoruesi.setRoliID(roliComboBoxModel.getElementAt(cmbRoli.getSelectedIndex()));
                    perdoruesiRepository.create(perdoruesi);
                    JOptionPane.showMessageDialog(this, "E dhÃ«na u ruajt me sukses !");
                } else {
                    Perdoruesi perdoruesi = this.perdoruesiTableModel.getPerdoruesi(row);

                    perdoruesi.setUsername(txtUsername.getText());
                    perdoruesi.setPassword(txtPassword.getText());
                    perdoruesi.setRoliID(roliComboBoxModel.getElementAt(cmbRoli.getSelectedIndex()));
                    perdoruesiRepository.create(perdoruesi);
                    perdoruesiRepository.edit(perdoruesi);
                    JOptionPane.showMessageDialog(this, "E dhÃ«na u editua me sukses");
                }
                emptyObject();
                tabelaLoad();
            }

        } catch (PerdoruesiException pe) {
            JOptionPane.showMessageDialog(this, pe.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

