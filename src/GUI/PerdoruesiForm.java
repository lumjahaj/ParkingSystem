    private void tabelaLoad() {
        List<Perdoruesi> lista = perdoruesiRepository.findAll();
        perdoruesiTableModel.add(lista);
        jTable1.setModel(perdoruesiTableModel);
        perdoruesiTableModel.fireTableDataChanged();
        tabelaSelectedIndexChange();
    }
    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = jTable1.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Perdoruesi perdoruesi = perdoruesiTableModel.getPerdoruesi(selectedIndex);
                    
                    txtUsername.setText(perdoruesi.getUsername());
                    txtPassword.setText(perdoruesi.getPassword());
                    cmbRoli.setSelectedItem(perdoruesi.getRoliID());
                    cmbRoli.repaint();
                }
            }
        });
    }

